
public class PlanoSaude {
	private String nome;
	private String cnpj;
	private Endereco EnderecoPS;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public Endereco getEnderecoPS() {
		return EnderecoPS;
	}
	public void setEnderecoPS(Endereco enderecoPS) {
		EnderecoPS = enderecoPS;
	}
	
	
}
